# ## String.capitalize():
# ### Capitalizes the string first character so String.capitalize() returns a copy of String with 
# ### the first character converted to uppercase and all other characters converted to lowercase:

# In[8]:


string = 'python is powerful language'
string.capitalize() 


# In[7]:


# Non-alphabetic characters not affected by this String method:
string = '1st programming language'
string.capitalize() 


# ## String.swapcase():
# ### Swaps case of alphabetic characters so it returns a copy of String with uppercase alphabetic 
# ### characters converted to lowercase and vice versa:

# In[6]:


string = 'pYTHON IS POWERFUL PROGRAMMING LANGUAGE'
string.swapcase() 


# ## String.title():
# ### Converts the string to title case, make it appears as a title.
# ### So string.title() returns a copy of string in which the first letter of each word is converted to uppercase and remaining letters are lowercase:

# In[3]:


string = 'Python the powerful programming language'
string.title()


